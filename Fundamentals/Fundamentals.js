//1. Sigma :
function sigma(x) {
	var sum = 0;
	for (var i = 1; i <= x; i++) {
		sum += i;
	}
	console.log(sum);
}

//sigma(3);

//2. Factorial :
function factorial(x) {
	var sum = 1;
	for (var i = 1; i <= x; i++) {
		sum *= i;
	}
	console.log(sum);
}

